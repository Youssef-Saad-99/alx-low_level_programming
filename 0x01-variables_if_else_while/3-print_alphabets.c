#include <stdio.h>

/**
 * main - Entry point
 * Description: print all alphabet in lowercase and uppercase
 * Return: 0 (Success)
*/

int main(void)
{
	char ch = 'a';
	char ch1 = 'A';

	while (ch <= 'z')
	{
		putchar(ch);
		ch++;
	}
	while (ch1 <= 'Z')
	{
		putchar(ch2);
		ch1++;
	}
	putchar('\n');

	return (0);
}
